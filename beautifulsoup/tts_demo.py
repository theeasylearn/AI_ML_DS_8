# Create speech → write directly to memory (no file on disk)
tts = gTTS(text=text, lang='gu', slow=False)
tts.save("my_gujarati_horoscope.mp3")
mp3_buffer = BytesIO()
tts.write_to_fp(mp3_buffer)
